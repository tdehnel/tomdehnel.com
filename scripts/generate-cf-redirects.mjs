#!/usr/bin/env node
// Generate public/_redirects (Cloudflare Pages / Netlify format) so that
// migrated URLs return real 301s at the edge instead of HTML meta-refresh.
//
// Source: astro-redirects.generated.json (produced by scripts/migrate.mjs)
// plus a small set of manual redirects (mirrors astro.config.mjs).
//
// Run automatically as a prebuild step. Safe to run by hand any time.

import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");

const generated = JSON.parse(
  readFileSync(join(ROOT, "astro-redirects.generated.json"), "utf8"),
);

// Keep in sync with the `manualRedirects` object in astro.config.mjs.
const manual = {
  "/about-me": "/about",
};

const all = { ...generated, ...manual };

const lines = [
  "# Generated by scripts/generate-cf-redirects.mjs. Edit the source files, not this file.",
  "# Cloudflare Pages reads this and applies real 301s at the edge.",
];
for (const [from, to] of Object.entries(all)) {
  // Normalize: ensure leading slash, no trailing slash (Cloudflare matches both).
  const f = from.replace(/\/+$/, "");
  const t = to.replace(/\/+$/, "");
  lines.push(`${f}\t${t}\t301`);
}

const outPath = join(ROOT, "public", "_redirects");
writeFileSync(outPath, lines.join("\n") + "\n");
console.error(`Wrote ${Object.keys(all).length} redirects to ${outPath}`);
